#include <stdio.h>
int main() {
    char nome [50];
    int idade;
    float altura;
    printf("Digite seu primeiro nome: ");
    scanf("%s", nome);
    printf("Digite sua idade: ");
    scanf("%d", &idade);
    printf ("Digite sua altura: ");
    scanf ("%2f" , &altura);
    printf("Seu nome e: %s\n", nome);
    printf("Sua idade e: %d\n", idade);
    printf ("Sua altura e: %f", altura);
    return 0; 
}



    

    




